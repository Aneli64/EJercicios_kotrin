package ejercicios_practica_internet.ejs_varios.minijuegos.carrera/*
Escribe un programa que simule una carrera de Mario Kart. El programa deberá tener
las siguientes características:
Habrá un número fijo de corredores (por ejemplo, 8).
Cada corredor tendrá un nombre y un kart asociado.
Cada kart tendrá una velocidad base y una probabilidad de aceleración crítica.
La velocidad base será un número entero entre 1 y 10, y la probabilidad de
aceleración crítica será un número decimal entre 0 y 1.
La carrera consistirá en varias rondas, cada una de las cuales representará un tramo del circuito.
En cada ronda, cada corredor avanzará un número de casillas igual a su velocidad base.
Si un corredor saca una aceleración crítica (con una probabilidad igual a su probabilidad
 de aceleración crítica), avanzará
 un número adicional de casillas igual a su velocidad base. La carrera terminará cuando un
 corredor llegue a la meta, es decir, avance un número de casillas igual o superior a la
 longitud del circuito. Tu programa deberá mostrar el progreso de la carrera en tiempo real,
indicando la posición de cada corredor en cada ronda. Al finalizar la carrera,
deberá mostrar el nombre del ganador.
 */